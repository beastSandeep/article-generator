const { normalizeArticle, renderLatex } = require("./src/latex");
const { parseArticleText } = require("./src/extract");
const fs = require("node:fs/promises");
const path = require("node:path");
const { execFile } = require("node:child_process");
const util = require("node:util");

const execFileAsync = util.promisify(execFile);

async function verify() {
  console.log("--- Starting Comprehensive Verification ---\n");

  // 1. Test Math and Table Rendering
  console.log("Test 1: Math and Table Rendering...");
  const articleData = {
    title: "Verification of Rich Content",
    authors: "Alice Tester",
    journalShort: "Generator Testing Journal",
    peerReviewText: "VERIFIED BUILD",
    articleType: "Technical Report",
    bodyText: `This paragraph tests inline math $ \\alpha + \\beta = \\gamma $ and special characters like & and %.

Here is a complex block formula generated by the utility:
$$ \\sigma = \\sqrt{\\frac{\\sum_{i=1}^{n} (x_i - \\mu)^2}{N}} $$

Below is a table generated by the Table Builder:
\\begin{center}
\\begin{tabular}{|l|l|l|}
\\hline
\\textbf{Utility} & \\textbf{Status} & \\textbf{Score} \\\\
\\hline
Math Helper & Working & 100\\% \\\\
\\hline
Table Builder & Verified & 10/10 \\\\
\\hline
Live Preview & Enabled & Yes \\\\
\\hline
\\end{tabular}
\\end{center}

Final paragraph with a DOI link: https://doi.org/10.1234/test.verify`
  };

  const normalized = normalizeArticle(articleData);
  const tex = renderLatex(normalized);

  // Verification Checks
  const checks = [
    { name: "Math preservation ($$)", pass: tex.includes("$$ \\sigma =") },
    { name: "Table preservation (\\begin{tabular})", pass: tex.includes("\\begin{tabular}") },
    { name: "Correct & handling in tables", pass: tex.includes("Utility} & \\textbf{Status}") },
    { name: "No double-escaping of backslashes", pass: !tex.includes("\\textbackslash{}begin") },
    { name: "Dynamic Header (Journal Name)", pass: tex.includes("Generator Testing Journal") },
    { name: "Dynamic Header (Center Text)", pass: tex.includes("VERIFIED BUILD") }
  ];

  let allPassed = true;
  checks.forEach(c => {
    console.log(`${c.pass ? "✅" : "❌"} ${c.name}`);
    if (!c.pass) allPassed = false;
  });

  if (!allPassed) {
    console.error("\nLogic verification failed! Inspecting LaTeX...");
    await fs.writeFile("debug-fail.tex", tex);
    process.exit(1);
  }

  // 2. Test PDF Compilation
  console.log("\nTest 2: PDF Compilation...");
  const texPath = path.join(__dirname, "generated", "verify-build.tex");
  await fs.mkdir(path.join(__dirname, "generated"), { recursive: true });
  await fs.writeFile(texPath, tex, "utf8");

  try {
    await execFileAsync("xelatex", [
      "-interaction=nonstopmode",
      "-halt-on-error",
      "-output-directory",
      path.join(__dirname, "generated"),
      texPath,
    ], { timeout: 60000 });
    console.log("✅ PDF compiled successfully!");
  } catch (err) {
    console.error("❌ PDF compilation failed!");
    console.log("Log tail:", err.stdout ? err.stdout.slice(-500) : err.message);
    process.exit(1);
  }

  console.log("\n--- All Verifications Passed! ---");
}

verify();
